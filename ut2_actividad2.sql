CREATE DATABASE IF NOT EXISTS ut2_actividad2;

CREATE TABLE IF NOT EXISTS departamentos(
dept_no TINYINT(2) NOT NULL PRIMARY KEY,
dnombre VARCHAR(15) NOT NULL,
localidad VARCHAR(15) NOT NULL
);

CREATE TABLE IF NOT EXISTS empleados(
emp_no TINYINT(2) NOT NULL PRIMARY KEY,
apellido VARCHAR(10) NOT NULL,
oficio VARCHAR(10) NOT NULL,
director SMALLINT(6),
fecha_alta DATE NOT NULL,
salario FLOAT(6,2) NOT NULL,
comision FLOAT(6,2),
dept_no TINYINT(2) NOT NULL,
FOREIGN KEY (dept_no) REFERENCES departamentos(dept_no)
);

